package com.pony.model;

public class TypeModel {

	private String typeCreatetime;
	private String typeName;
	private String typeTeaName;
	private String typeTeaId;
	private String typeId;

	public String getTypeCreatetime() {
		return typeCreatetime;
	}

	public void setTypeCreatetime(String typeCreatetime) {
		this.typeCreatetime = typeCreatetime;
	}

	public String getTypeName() {
		return typeName;
	}

	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}

	public String getTypeTeaName() {
		return typeTeaName;
	}

	public void setTypeTeaName(String typeTeaName) {
		this.typeTeaName = typeTeaName;
	}

	public String getTypeTeaId() {
		return typeTeaId;
	}

	public void setTypeTeaId(String typeTeaId) {
		this.typeTeaId = typeTeaId;
	}

	public String getTypeId() {
		return typeId;
	}

	public void setTypeId(String typeId) {
		this.typeId = typeId;
	}

}
